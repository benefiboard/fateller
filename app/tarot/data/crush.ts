export const getCrushCardById = (id: string): SingleTarotCard | undefined => {
  return CRUSH_TAROT_CARDS.find((card) => card.id === id);
};

// 랜덤 해석 선택
export const getRandomCrushInterpretation = (card: SingleTarotCard) => {
  const randomIndex = Math.floor(Math.random() * card.interpretation.length);
  return card.interpretation[randomIndex];
};
