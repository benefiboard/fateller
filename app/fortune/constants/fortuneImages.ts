export const getRandomImage = (
  state: 'positive' | 'neutral' | 'negative'
): {
  imageUrl: string;
  description: string;
} => {
  const images = FORTUNE_IMAGES[state];
  const randomIndex = Math.floor(Math.random() * images.length);
  return {
    imageUrl: images[randomIndex].url,
    description: images[randomIndex].description,
  };
};
